<template>
  <div class="select-cus-popup">
    <q-card-section class="row items-center pop-head">
      <h6>{{ $q.lang.addRecipes.PopUpTitle.cuisineType }}</h6>
      <q-space />
      <q-btn icon="close" flat round dense v-close-popup />
    </q-card-section>
    <q-card-section class="pop-body">
      <div class="port-type">
        <div class="radio-cus-popup">
          <q-option-group
            :options="cuisines"
            type="radio"
            v-model="val"
            @update:model-value="setData"
          />
        </div>
      </div>
      <!-- <div class="absolute-bottom q-px-lg q-py-md box-shadow-cus">
        <q-btn
          class="btn full-width btn1"
          flat
          dense
          @click="setData()"
        >
          Done
        </q-btn>
      </div> -->
    </q-card-section>
  </div>
</template>

<script>
import { ref } from 'vue';
import { useStore } from 'vuex'

export default {
  name: "CuisineType",
  props: {
    sendValue : {
      type: String,
      default: '',
    },
    fieldName: {
      type: String,
      default: '',
    },
    sendDisplayField: {
      type: String,
      default: null,
    }
  },
  setup () {
    const store = useStore();

    let cuisines = store.getters['master/getCuisineTypeOption'];
    if(!cuisines || cuisines.length === 0) {
      store.dispatch('master/getCuisines');
    }

    return {
      val: ref(null)
    }
  },
  computed: {
    cuisines() {
      return this.$store.getters['master/getCuisineTypeOption'];
    }
  },
  created(){
    if(this.sendValue) {
      this.val = this.sendValue;
    }
  },
  methods:{
    setData() {
      this.$emit('selectValue', {
        value: this.val,
        field: this.fieldName
      })

      if(this.sendDisplayField !== null) {
        this.$emit('selectValue', {
          value: this.cuisines.find(item => item.value === this.val).label,
          field: this.sendDisplayField
        })
      }
    }
  }
}
</script>
